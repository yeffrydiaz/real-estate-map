export function toGeoJSON(properties: Property[]): PropertyGeoJSONCollection {
  return {
    type: "FeatureCollection",
    features: properties.map((property) => ({
      type: "Feature",
      geometry: {
        type: "Point",
        coordinates: [property.longitude, property.latitude],
      },
      properties: property,
    })),
  };
}

export function formatPrice(
  price: number,
  status: Property["status"]
): string {
  if (status === "for_rent") {
    return `$${price.toLocaleString()}/mo`;
  }
  if (price >= 1_000_000) {
    return `$${(price / 1_000_000).toFixed(2)}M`;
  }
  return `$${price.toLocaleString()}`;
}

export function getStatusLabel(status: Property["status"]): string {
  const labels: Record<Property["status"], string> = {
    for_sale: "For Sale",
    for_rent: "For Rent",
    sold: "Sold",
    pending: "Pending",
  };
  return labels[status];
}

export function getStatusColor(status: Property["status"]): string {
  const colors: Record<Property["status"], string> = {
    for_sale: "bg-emerald-100 text-emerald-800",
    for_rent: "bg-blue-100 text-blue-800",
    sold: "bg-gray-100 text-gray-800",
    pending: "bg-amber-100 text-amber-800",
  };
  return colors[status];
}
